public class SeatingArrangements {
  
  public static void main(String[] args) {
    for (int row = 1; row <= 3; row++) {
      System.out.println("Row " + row + ":");
    for (int seat = 1; seat <= 5; seat++) {
      System.out.println("Seat" + seat);
    }
    System.out.println("----------");
    }
  }
}

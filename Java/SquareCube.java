public class SquareCube {
          
      public static void main(String[] args){
         
       int square = 0;
       int cube = 0;

	  System.out.println("number"    +    "square" +   "cube");
           
       
    for(int i = 0; i <= 10; i++){
    square = i * i;
    cube = i * i * i;
    
    System.out.println(i  +"       " + square + "      " +   cube);
}    
}
}
